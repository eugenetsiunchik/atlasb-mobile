function isCoordinateInsideRing(coordinate: MapCoordinate, ring: Array<[number, number]>) {
  let isInside = false;

  for (let index = 0, previousIndex = ring.length - 1; index < ring.length; previousIndex = index, index += 1) {
    const [currentLongitude, currentLatitude] = ring[index] ?? [0, 0];
    const [previousLongitude, previousLatitude] = ring[previousIndex] ?? [0, 0];
    const intersects =
      currentLatitude > coordinate.latitude !== previousLatitude > coordinate.latitude &&
      coordinate.longitude <
        ((previousLongitude - currentLongitude) *
          (coordinate.latitude - currentLatitude)) /
          (previousLatitude - currentLatitude) +
          currentLongitude;

    if (intersects) {
      isInside = !isInside;
    }
  }

  return isInside;
}

export function isCoordinateInsideHardcodedCityBoundary(coordinate: MapCoordinate) {
  return HARDCODED_BELARUS_CITY_BOUNDARIES.some(cityBoundary => {
    const { bounds } = cityBoundary;

    if (
      coordinate.latitude < bounds.minLatitude ||
      coordinate.latitude > bounds.maxLatitude ||
      coordinate.longitude < bounds.minLongitude ||
      coordinate.longitude > bounds.maxLongitude
    ) {
      return false;
    }

    return cityBoundary.polygons.some(polygon => isCoordinateInsideRing(coordinate, polygon));
  });
}
